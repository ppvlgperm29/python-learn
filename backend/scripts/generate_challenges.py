order = 23  # начинаем после существующих 22

def c(id, difficulty, category, title, description, starter, tests, hint):
    global order
    ch = {
        "id": id,
        "order": order,
        "difficulty": difficulty,
        "category": category,
        "title": title,
        "description": description,
        "starter_code": starter,
        "tests": tests,
        "hint": hint,
    }
    challenges.append(ch)
    order += 1

def t(args, expected, label=None):
    return {"args": args, "expected": expected, "label": label or f"solution({', '.join(repr(a) for a in args)})"}

